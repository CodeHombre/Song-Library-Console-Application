void list_song(Song s){
    cout<<"SONG: "<<s.name<<" | ALBUM: "<<s.album<<" | ARTIST: "<<s.artist<<" | DURATION: "<<s.duration<<'\n';
}

void play_song(Song s){
    cout<<"--------------------------------------------------------------------------------------------------------------------\n";
    cout<<"PLAYING...\n";
    cout<<s.name<<'\n';
    cout<<s.album<<'\n';
    cout<<s.artist<<'\n';
    cout<<"--------------------------------------------------------------------------------------------------------------------\n";
}

void show_playlist(Playlist p){
    cout<<"--------------------------------------------------------------------------------------------------------------------\n";
    cout<<"PLAYLIST: "<<p.name<<'\n';
    cout<<"CREATED BY: "<<p.creator<<'\n';
    cout<<"--------------------------------------------------------------------------------------------------------------------\n";
    for(map<string, Song>::iterator it=p.songs.begin(); it!=p.songs.end(); it++){
        list_song(it->second);
    }
    cout<<"--------------------------------------------------------------------------------------------------------------------\n";
}
